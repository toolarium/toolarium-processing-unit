package com.github.toolarium.processing.unit.dto;


/**
 * Defines the parameter value type.
 *
 * @author patrick
 */
public enum ParameterValueType {
    
    /** BOOLEAN */
    BOOLEAN,

    /** CHAR */
    CHAR,

    /** STRING */
    STRING,

    /** SHORT */
    SHORT,

    /** INTEGER */
    INTEGER,

    /** LONG */
    LONG,

    /** FLOAT */
    FLOAT,

    /** DOUBLE */
    DOUBLE,

    /** DATE */
    DATE,

    /** TIME */
    TIME,

    /** DATETIME */
    DATETIME,

    /** REGEXP: regular expression */
    REGEXP
}
